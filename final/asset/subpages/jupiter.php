            <h2>Atmostphere</h2>
            <p>The atmosphere of Jupiter extends to a depth of 3,000 km (2,000 mi) below the cloud layers.

                Jupiter is perpetually covered with clouds of ammonia crystals, which may contain ammonium hydrosulfide
                as well. The clouds are located in the tropopause layer of the atmosphere, forming bands at different
                latitudes, known as tropical regions. These are subdivided into lighter-hued zones and darker belts. The
                interactions of these conflicting circulation patterns cause storms and turbulence. Wind speeds of 100
                meters per second (360 km/h; 220 mph) are common in zonal jet streams. The zones have been observed to
                vary in width, color and intensity from year to year, but they have remained stable enough for
                scientists to name them.

                The cloud layer is about 50 km (31 mi) deep, and consists of at least two decks of ammonia clouds: a
                thin clearer region on top with a thick lower deck. There may be a thin layer of water clouds underlying
                the ammonia clouds, as suggested by flashes of lightning detected in the atmosphere of Jupiter. These
                electrical discharges can be up to a thousand times as powerful as lightning on Earth. The water clouds
                are assumed to generate thunderstorms in the same way as terrestrial thunderstorms, driven by the heat
                rising from the interior. The Juno mission revealed the presence of "shallow lightning" which originates
                from ammonia-water clouds relatively high in the atmosphere. These discharges carry "mushballs" of
                water-ammonia slushes covered in ice, which fall deep into the atmosphere. Upper-atmospheric lightning
                has been observed in Jupiter's upper atmosphere, bright flashes of light that last around 1.4
                milliseconds. These are known as "elves" or "sprites" and appear blue or pink due to the hydrogen.

                The orange and brown colors in the clouds of Jupiter are caused by upwelling compounds that change color
                when they are exposed to ultraviolet light from the Sun. The exact makeup remains uncertain, but the
                substances are thought to be made up of phosphorus, sulfur or possibly hydrocarbons. These colorful
                compounds, known as chromophores, mix with the warmer clouds of the lower deck. The light-coloured zones
                are formed when rising convection cells form crystallizing ammonia that hides the chromophores from
                view.
            </p>
