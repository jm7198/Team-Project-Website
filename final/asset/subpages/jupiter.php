            <h2>Geology</h2>
            <p>Before the early 21st century, most scientists proposed one of two scenarios for the formation of
                Jupiter. If the planet accreted first as a solid body, it would consist of a dense core, a surrounding
                layer of liquid metallic hydrogen (with some helium) extending outward to about 80% of the radius of the
                planet, and an outer atmosphere consisting primarily of molecular hydrogen. Alternatively, if the planet
                collapsed directly from the gaseous protoplanetary disk, it was expected to completely lack a core,
                consisting instead of denser and denser fluid (predominantly molecular and metallic hydrogen) all the
                way to the center. Data from the Juno mission showed that Jupiter has a very diffuse core that mixes
                into its mantle. This mixing process could have arisen during formation, while the planet accreted
                solids and gasses from the surrounding nebula. Alternatively, it could have been caused by an impact
                from a planet of about ten Earth masses a few million years after Jupiter's formation, which would have
                disrupted an originally solid Jovian core. It is estimated that the core takes up 30–50% of the planet's
                radius, and contains heavy elements with a combined mass 7–25 times the Earth.

                Outside the layer of metallic hydrogen lies a transparent interior atmosphere of hydrogen. At this
                depth, the pressure and temperature are above molecular hydrogen's critical pressure of 1.3 MPa and
                critical temperature of 33 K (−240.2 °C; −400.3 °F). In this state, there are no distinct liquid and gas
                phases—hydrogen is said to be in a supercritical fluid state. The hydrogen and helium gas extending
                downward from the cloud layer gradually transitions to a liquid in deeper layers, possibly resembling
                something akin to an ocean of liquid hydrogen and other supercritical fluids. Physically, the gas
                gradually becomes hotter and denser as depth increases.

                Rain-like droplets of helium and neon precipitate downward through the lower atmosphere, depleting the
                abundance of these elements in the upper atmosphere. Calculations suggest that helium drops separate
                from metallic hydrogen at a radius of 60,000 km (37,000 mi) (11,000 km (6,800 mi) below the cloud tops)
                and merge again at 50,000 km (31,000 mi) (22,000 km (14,000 mi) beneath the clouds). Rainfalls of
                diamonds have been suggested to occur, as well as on Saturn and the ice giants Uranus and Neptune.

                The temperature and pressure inside Jupiter increase steadily inward because the heat of planetary
                formation can only escape by convection. At a surface depth where the atmospheric pressure level is 1
                bar (0.10 MPa), the temperature is around 165 K (−108 °C; −163 °F). The region of supercritical hydrogen
                changes gradually from a molecular fluid to a metallic fluid spans pressure ranges of 50–400 GPa with
                temperatures of 5,000–8,400 K (4,730–8,130 °C; 8,540–14,660 °F), respectively. The temperature of
                Jupiter's diluted core is estimated to be 20,000 K (19,700 °C; 35,500 °F) with a pressure of around
                4,000 GPa.

            </p>
