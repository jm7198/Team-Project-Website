            <p>The total surface area of Earth is about 510 million km2 (197 million sq mi). Earth's surface can be
                divided into two hemispheres, such as into the Northern and southern hemisphere, or the Western and
                Eastern Hemisphere
                Most of the surface is made of water, in liquid form or in smaller amounts as ice. 70.8% (361.13 million
                km2 (139.43 million sq mi)) of the Earth's surface consists of the interconnected ocean, making it
                Earth's global ocean or world ocean. This makes Earth, along with its vibrant hydrosphere a water world
                or ocean world, particularly in Earth's early history when the ocean is thought to have possibly covered
                Earth completely. The world ocean is commonly divided into the Pacific Ocean, Atlantic Ocean, Indian
                Ocean, Southern Ocean and Arctic Ocean, from largest to smallest. Below the ocean's surface are the
                continental shelf, mountains, volcanoes, oceanic trenches, submarine canyons, oceanic plateaus, abyssal
                plains, and a globe-spanning mid-ocean ridge system.
                In contrast, Earth's land makes up 29.2%, or 148.94 million km2 (57.51 million sq mi) of Earth's surface
                area. Earth's land consists of many islands around the globe, but mainly of four continental landmasses,
                which are from largest to smallest: Afroeurasia,America, Antarctica and Australia. These land masses are
                further broken down and grouped into the continents. The terrain varies greatly and consists of
                mountains, deserts, plains, plateaus, and other landforms. The elevation of the land surface varies from
                the low point of −418 m (−1,371 ft) at the Dead Sea, to a maximum altitude of 8,848 m (29,029 ft) at the
                top of Mount Everest. The mean height of land above sea level is about 797 m (2,615 ft).
                The continental crust consists of lower density material such as the igneous rocks granite and andesite.
                Less common is basalt, a denser volcanic rock that is the primary constituent of the ocean floors.
                Sedimentary rock is formed from the accumulation of sediment that becomes buried and compacted together.
                Nearly 75% of the continental surfaces are covered by sedimentary rocks, although they form about 5% of
                the crust. The third form of rock material found on Earth is metamorphic rock, which is created from the
                transformation of pre-existing rock types through high pressures, high temperatures, or both. The most
                abundant silicate minerals on Earth's surface include quartz, feldspars, amphibole, mica, pyroxene and
                olivine. Common carbonate minerals include calcite (found in limestone) and dolomite.
                Erosion and tectonics, volcanic eruptions, flooding, weathering, glaciation, the growth of coral reefs,
                and meteorite impacts are among the processes that constantly reshape Earth's surface over geological
                time. The pedosphere is the outermost layer of Earth's continental surface and is composed of soil and
                subject to soil formation processes. The total arable land is 10.9% of the land surface, with 1.3% being
                permanent cropland. Close to 40% of Earth's land surface is used for agriculture, or an estimated 16.7
                million km2 (6.4 million sq mi) of cropland and 33.5 million km2 (12.9 million sq mi) of pastureland.
